CString GetUTF8Str( CString &str )
{
	CString r(L"");
	DWORD chCount = 0;
	DWORD size = str.GetLength() * 10;
	LPTSTR buf = r.GetBuffer(size);
	AtlEscapeUrl(str, buf, &chCount, size, ATL_URL_BROWSER_MODE|ATL_URL_ENCODE_PERCENT);
	r.ReleaseBuffer();
	return r;
}

